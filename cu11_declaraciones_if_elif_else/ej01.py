# Declaracion if, elif y else

"""
• Usamos las declaraciones para controlar el flujo de nuestra aplicacion

• Usualmente solo queremos que cierto código sea ejecutado cuando
  una condicion particular ocurra

• Por ejemplo, IF mi perro tiene hambre (Aplicar Logica), Else (aplicar logica
  si perro no tiene hambre)

• Python usa un Sistema de indentación al momento que declaramos funciones y logica
  con IF

• Se llama "Control Flow Syntax" y usamos : e Indetacion (espacios en blanco)

• Este sistema de Indentacion es muy importante y es lo que separa a python
  del resto de lenguajee

• Sintaxis de una declaracion IF

if alguna_condicion:
    # Ejecutamos código

• Sintaxis de una declaracion IF/ElSE

if alguna_condicion:
    # Ejecutamos codigo
else:
    #Aplicar algo más

• Sintaxis de una declaracion ELIF

if alguna_condicion:
    # Ejecutamos codigo
elif alguna_otra condicion:
    # algo distinto
else:
    # hacer algo más
"""

if True:
    print(True)
