# los elementos de revés pero si han ordenado se añadir el sort()
###
mi_lista = ['a', 'z', 'x', 'b', 'd']
num_lista = [4, 1, 5, 7, 3]

mi_lista.sort() # la lista para ordenar a los elemntos
mi_lista.reverse() # la lista para ordenar a los elemntos de revés

num_lista.sort() # la lista para ordenar a los numeros
num_lista.reverse() # la lista para ordenar a los numeros de revés

print(mi_lista)
# ['z', 'x', 'd', 'b', 'a']

print(num_lista)
# [7, 5, 4, 3, 1]