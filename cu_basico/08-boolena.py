"""
-Booleanos

• Valores que permiten declarar Verdadero o Falso
• Son muy importantes cuando mas adelatne trabajemos con logica
"""

print(type(False))
# > <class 'bool'>

print(type(True))
# > <class 'bool'>

print(1 > 2)
# > False

print(2 > 2)
# > False

print(2 >= 2)
# > True

print(2 == 2)
# > True

print(2 == 3)
# > False

